// Operadores Aritmeticos

// Variables
let n1 = 10
let n2 = 25
let n3 = 109
let n4 = 90
let n5 = 2

// Suma
let resSuma = n1 + n2 + n3 + n4
console.log(resSuma); // output -> 234

// Resta
let resResta = n2 - n1
console.log(resResta); // output -> 15

// Division o Cociente
let resDivisión = n4 / n1
console.log(resDivisión); // output -> 9

// Multiplicación
let resMultiplicación = n3 * n2
console.log(resMultiplicación); // output -> 2725

// Potencia
let resPotencia = n1 ** n5
console.log(resPotencia); // output -> 100

// Modulo o Residuo
let resModulo = n2 % n1
console.log(resModulo); // output -> 5